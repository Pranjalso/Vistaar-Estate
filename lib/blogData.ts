// Helper functions
export function getPostBySlug(slug: string) {
  return blogPosts.find(post => post.slug === slug)
}

export function getRelatedPosts(slug: string, limit: number = 3) {
  const currentPost = getPostBySlug(slug)
  if (!currentPost) return []
  
  // Get all posts except the current one
  const otherPosts = blogPosts.filter(p => p.id !== currentPost.id)
  
  // First try to get posts from same category
  const sameCategory = otherPosts.filter(p => p.category === currentPost.category)
  
  // If we have enough in same category, return those
  if (sameCategory.length >= limit) {
    return sameCategory.slice(0, limit)
  }
  
  // Otherwise, return a mix of same category and other posts
  const remaining = otherPosts.filter(p => p.category !== currentPost.category)
  const combined = [...sameCategory, ...remaining]
  return combined.slice(0, limit)
}

export function getAllCategories() {
  const categories = blogPosts.map(post => post.category)
  return ['All', ...new Set(categories)]
}